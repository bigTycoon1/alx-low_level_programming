#include "3-calc.h"

/**
 * op_add - the prototype
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - prototype to subtract
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - prototype to multiply
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - prototype to divide
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - prototype to return remainder
 * @a: integer
 * @b: integer
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
